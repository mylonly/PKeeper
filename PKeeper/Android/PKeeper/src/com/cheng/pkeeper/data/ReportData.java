package com.cheng.pkeeper.data;

import java.io.Serializable;

public class ReportData implements Serializable {
	
	private String cretime; // 报告提交时间
	private String desc;     //报告的内容 
	private String resdesc;  //专家回复的内容（专家回复后才有值）
	private String pic;     // 报告提交的图片路径
	private String title;    // 报告标题
	private String resname;   //回复的专家名称（专家回复后才有值）
	private String restime;  //专家回复的时间（专家回复后才有值）
	private int resuserid;
	private int isread;
	private int id;
	
	public String getCretime() {
		return cretime;
	}
	public void setCretime(String cretime) {
		this.cretime = cretime;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getResdesc() {
		return resdesc;
	}
	public void setResdesc(String resdesc) {
		this.resdesc = resdesc;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getResname() {
		return resname;
	}
	public void setResname(String resname) {
		this.resname = resname;
	}
	public String getRestime() {
		return restime;
	}
	public void setRestime(String restime) {
		this.restime = restime;
	}
	public int getResuserid() {
		return resuserid;
	}
	public void setResuserid(int resuserid) {
		this.resuserid = resuserid;
	}
	public int getIsread() {
		return isread;
	}
	public void setIsread(int isread) {
		this.isread = isread;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}
