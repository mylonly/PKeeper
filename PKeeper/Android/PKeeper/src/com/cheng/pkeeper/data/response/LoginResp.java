package com.cheng.pkeeper.data.response;

import com.cheng.pkeeper.data.CFactoryData;
import com.cheng.pkeeper.data.UserData;

public class LoginResp extends BasicResponse {
	
	private Boolean flag;
	private UserData userdata;
	private CFactoryData cfactorydata;
	
	public LoginResp(){
		
	}
	
	public LoginResp(Boolean flag) {
		
		this.flag = flag;
	}
	public Boolean getFlag() {
		return flag;
	}
	public void setFlag(Boolean flag) {
		this.flag = flag;
	}

	public UserData getUserdata() {
		return userdata;
	}

	public void setUserdata(UserData userdata) {
		this.userdata = userdata;
	}

	public CFactoryData getCfactorydata() {
		return cfactorydata;
	}

	public void setCfactorydata(CFactoryData cfactorydata) {
		this.cfactorydata = cfactorydata;
	}

	

}
